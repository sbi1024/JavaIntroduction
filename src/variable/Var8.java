package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i = 2147483647;
        long l = 10L;

        // 실수
        float f = 10.0f;
        double d = 10.0;
        
        // 클래스는 대문자로 시작, 나머지는 소문자로 시작
        // 카멜케이스로 네이밍 
        // 상수만 대문자로 네이밍
    }
}
