package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 20; //  변수 초기화, 이부분에서 값을 변경함에 따라 하기 출력문에 값이 변경된다.

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        // 자바에서 = 는 같다의 뜻이 아니라 오른쪽에 있는 값을 왼쪽 변수에 담는다 라는 뜻이다.
    }
}
